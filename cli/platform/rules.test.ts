import * as fs from "node:fs";
import { afterEach, beforeEach, describe, expect, it, vi } from "vitest";
import {
  generateClaudeRules,
  generateCursorRules,
  mergeRulesIndexForVendor,
  readRules,
} from "./rules.js";

vi.mock("node:fs", () => ({
  existsSync: vi.fn(),
  mkdirSync: vi.fn(),
  readFileSync: vi.fn(),
  readdirSync: vi.fn(),
  writeFileSync: vi.fn(),
  cpSync: vi.fn(),
}));

const mockTargetDir = "/tmp/test-project";

describe("readRules", () => {
  beforeEach(() => vi.clearAllMocks());
  afterEach(() => vi.restoreAllMocks());

  it("should return empty array when rules dir does not exist", () => {
    (fs.existsSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      false,
    );
    expect(readRules(mockTargetDir)).toEqual([]);
  });

  it("should parse frontmatter fields from rule files", () => {
    (fs.existsSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      true,
    );
    (fs.readdirSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue([
      "frontend.md",
    ]);
    (fs.readFileSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      '---\ndescription: Frontend standards\nglobs: "**/*.tsx"\nalwaysApply: false\n---\n\n# Frontend',
    );

    const rules = readRules(mockTargetDir);
    expect(rules).toHaveLength(1);
    expect(rules[0]?.name).toBe("frontend");
    expect(rules[0]?.description).toBe("Frontend standards");
    expect(rules[0]?.globs).toBe("**/*.tsx");
    expect(rules[0]?.alwaysApply).toBe(false);
    expect(rules[0]?.body).toContain("# Frontend");
  });

  it("should filter non-.md files", () => {
    (fs.existsSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      true,
    );
    (fs.readdirSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue([
      ".gitkeep",
      "frontend.md",
    ]);
    (fs.readFileSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      "---\ndescription: test\n---\n\n# Test",
    );

    expect(readRules(mockTargetDir)).toHaveLength(1);
  });
});

describe("generateCursorRules", () => {
  beforeEach(() => vi.clearAllMocks());
  afterEach(() => vi.restoreAllMocks());

  it("should return empty array when no rules exist", () => {
    (fs.existsSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      false,
    );
    expect(generateCursorRules(mockTargetDir)).toEqual([]);
  });

  it("should generate .mdc files with Cursor frontmatter", () => {
    (fs.existsSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      true,
    );
    (fs.readdirSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue([
      "frontend.md",
    ]);
    (fs.readFileSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      '---\ndescription: Frontend standards\nglobs: "**/*.{tsx,jsx}"\nalwaysApply: false\n---\n\n# Frontend\n\n1. Use React',
    );

    const result = generateCursorRules(mockTargetDir);
    expect(result).toEqual(["frontend"]);

    const writeCall = (
      fs.writeFileSync as unknown as ReturnType<typeof vi.fn>
    ).mock.calls.find(
      (call: string[]) =>
        typeof call[0] === "string" && call[0].endsWith(".mdc"),
    );

    expect(writeCall).toBeDefined();
    const content = writeCall?.[1] as string;
    expect(content).toContain('description: "Frontend standards"');
    expect(content).toContain('globs: "**/*.{tsx,jsx}"');
    expect(content).toContain("alwaysApply: false");
    expect(content).toContain("# Frontend");
    expect(content).toContain("<!-- Generated by oh-my-agent");
  });

  it("should respect alwaysApply: true", () => {
    (fs.existsSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      true,
    );
    (fs.readdirSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue([
      "i18n.md",
    ]);
    (fs.readFileSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      "---\ndescription: i18n\nalwaysApply: true\n---\n\n# i18n",
    );

    generateCursorRules(mockTargetDir);

    const writeCall = (
      fs.writeFileSync as unknown as ReturnType<typeof vi.fn>
    ).mock.calls.find(
      (call: string[]) =>
        typeof call[0] === "string" && call[0].endsWith(".mdc"),
    );

    expect(writeCall?.[1] as string).toContain("alwaysApply: true");
  });
});

