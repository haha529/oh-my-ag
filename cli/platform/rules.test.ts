describe("mergeRulesIndexForVendor", () => {
  beforeEach(() => vi.clearAllMocks());
  afterEach(() => vi.restoreAllMocks());

  it("should return false for unsupported vendor", () => {
    expect(mergeRulesIndexForVendor(mockTargetDir, "unknown-vendor")).toBe(
      false,
    );
  });

  it("should still generate usage guide even without rules", () => {
    (fs.existsSync as unknown as ReturnType<typeof vi.fn>).mockImplementation(
      (p: string) => {
        if (typeof p === "string" && p.endsWith("GEMINI.md")) return false;
        if (typeof p === "string" && p.includes(".agents/rules")) return false;
        return false;
      },
    );

    const result = mergeRulesIndexForVendor(mockTargetDir, "gemini");
    expect(result).toBe(true);

    const writeCall = (
      fs.writeFileSync as unknown as ReturnType<typeof vi.fn>
    ).mock.calls.find(
      (call: string[]) =>
        typeof call[0] === "string" && call[0].endsWith("GEMINI.md"),
    );
    expect(writeCall).toBeDefined();
    const content = writeCall?.[1] as string;
    expect(content).toContain("# oh-my-agent");
    expect(content).toContain("## Workflows");
    expect(content).not.toContain("## Project Rules");
  });

  it("should create GEMINI.md with usage guide and rules index", () => {
    (fs.existsSync as unknown as ReturnType<typeof vi.fn>).mockImplementation(
      (p: string) => {
        const norm = typeof p === "string" ? p.replace(/\\/g, "/") : "";
        if (norm.endsWith("GEMINI.md")) return false;
        return norm.includes(".agents/rules");
      },
    );
    (fs.readdirSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue([
      "frontend.md",
      "backend.md",
    ]);
    (fs.readFileSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      '---\ndescription: test\nglobs: "**/*.tsx"\n---\n\n# Test',
    );

    const result = mergeRulesIndexForVendor(mockTargetDir, "gemini");
    expect(result).toBe(true);

    const writeCall = (
      fs.writeFileSync as unknown as ReturnType<typeof vi.fn>
    ).mock.calls.find(
      (call: string[]) =>
        typeof call[0] === "string" && call[0].endsWith("GEMINI.md"),
    );
    expect(writeCall).toBeDefined();
    const content = writeCall?.[1] as string;
    expect(content).toContain("<!-- OMA:START");
    expect(content).toContain("<!-- OMA:END -->");
    expect(content).toContain("# oh-my-agent");
    expect(content).toContain("## Workflows");
    expect(content).toContain("| frontend |");
    expect(content).toContain("| backend |");
    expect(content).toContain(".agents/rules/");
  });

  it("should create AGENTS.md for codex vendor", () => {
    (fs.existsSync as unknown as ReturnType<typeof vi.fn>).mockImplementation(
      (p: string) => {
        if (typeof p === "string" && p.endsWith("AGENTS.md")) return false;
        return p.includes(".agents/rules");
      },
    );
    (fs.readdirSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue([
      "frontend.md",
    ]);
    (fs.readFileSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      "---\ndescription: test\n---\n\n# Test",
    );

    mergeRulesIndexForVendor(mockTargetDir, "codex");

    const writeCall = (
      fs.writeFileSync as unknown as ReturnType<typeof vi.fn>
    ).mock.calls.find(
      (call: string[]) =>
        typeof call[0] === "string" && call[0].endsWith("AGENTS.md"),
    );
    expect(writeCall).toBeDefined();
  });

  it("should write CLAUDE.md for claude vendor", () => {
    (fs.existsSync as unknown as ReturnType<typeof vi.fn>).mockImplementation(
      (p: string) => {
        if (typeof p === "string" && p.endsWith("CLAUDE.md")) return false;
        return p.includes(".agents/rules");
      },
    );
    (fs.readdirSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue([
      "frontend.md",
    ]);
    (fs.readFileSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      "---\ndescription: test\n---\n\n# Test",
    );

    mergeRulesIndexForVendor(mockTargetDir, "claude");

    const writeCall = (
      fs.writeFileSync as unknown as ReturnType<typeof vi.fn>
    ).mock.calls.find(
      (call: string[]) =>
        typeof call[0] === "string" && call[0].endsWith("CLAUDE.md"),
    );
    expect(writeCall).toBeDefined();
  });

  it("should write AGENTS.md for qwen vendor (shared with codex)", () => {
    (fs.existsSync as unknown as ReturnType<typeof vi.fn>).mockImplementation(
      (p: string) => {
        if (typeof p === "string" && p.endsWith("AGENTS.md")) return false;
        return p.includes(".agents/rules");
      },
    );
    (fs.readdirSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue([
      "frontend.md",
    ]);
    (fs.readFileSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      "---\ndescription: test\n---\n\n# Test",
    );

    mergeRulesIndexForVendor(mockTargetDir, "qwen");

    const writeCall = (
      fs.writeFileSync as unknown as ReturnType<typeof vi.fn>
    ).mock.calls.find(
      (call: string[]) =>
        typeof call[0] === "string" && call[0].endsWith("AGENTS.md"),
    );
    expect(writeCall).toBeDefined();
  });

  it("should write AGENTS.md for cursor vendor", () => {
    (fs.existsSync as unknown as ReturnType<typeof vi.fn>).mockImplementation(
      (p: string) => {
        if (typeof p === "string" && p.endsWith("AGENTS.md")) return false;
        return p.includes(".agents/rules");
      },
    );
    (fs.readdirSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue([
      "frontend.md",
    ]);
    (fs.readFileSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      "---\ndescription: test\n---\n\n# Test",
    );

    mergeRulesIndexForVendor(mockTargetDir, "cursor");

    const writeCall = (
      fs.writeFileSync as unknown as ReturnType<typeof vi.fn>
    ).mock.calls.find(
      (call: string[]) =>
        typeof call[0] === "string" && call[0].endsWith("AGENTS.md"),
    );
    expect(writeCall).toBeDefined();
  });

  it("should replace existing OMA block on update", () => {
    const omaStart = "<!-- OMA:START";
    const omaEnd = "<!-- OMA:END -->";
    (fs.existsSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue(
      true,
    );
    (fs.readdirSync as unknown as ReturnType<typeof vi.fn>).mockReturnValue([
      "frontend.md",
    ]);
    (fs.readFileSync as unknown as ReturnType<typeof vi.fn>).mockImplementation(
      (p: string) => {
        if (typeof p === "string" && p.endsWith("AGENTS.md")) {
          return `# My Project\n\nCustom content\n\n${omaStart} -->\nold block\n${omaEnd}\n\nMore user content`;
        }
        return "---\ndescription: test\n---\n\n# Test";
      },
    );

    mergeRulesIndexForVendor(mockTargetDir, "codex");

    const writeCall = (
      fs.writeFileSync as unknown as ReturnType<typeof vi.fn>
    ).mock.calls.find(
      (call: string[]) =>
        typeof call[0] === "string" && call[0].endsWith("AGENTS.md"),
    );
    const content = writeCall?.[1] as string;
    expect(content).toContain("# My Project");
    expect(content).toContain("Custom content");
    expect(content).toContain("More user content");
    expect(content).toContain(omaStart);
    expect(content).not.toContain("old block");
    expect(content).toContain("# oh-my-agent");
  });
});
